    private void jList1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList1MouseClicked
        // TODO add your handling code here:
        // go to the selected issue based on the issue ID
        JList target = (JList) evt.getSource();
        int index = target.locationToIndex(evt.getPoint());
        History issueHistory = changeLog.get(index);
        int issueId = issueHistory.getIssueId();
        try {
            new IssuePage(projectIndex, issueId);
            dispose();
        } catch (SQLException ex) {
            Logger.getLogger(ChangeLog.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jList1MouseClicked

