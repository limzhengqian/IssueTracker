    /**
     * Scales images uploaded by user
     */
    private void scaleImage() {
        //create imageIcon of the picture
        ImageIcon imageIcon = new ImageIcon(filename);
        
        //get image from the imageIcon
        Image img = imageIcon.getImage();
        
        //create new image with scaled size of jLabel1
        Image newImg = img.getScaledInstance(jLabel1.getWidth(), jLabel1.getHeight(), Image.SCALE_SMOOTH);
        
        //initialize imageIcon with new image
        imageIcon = new ImageIcon(newImg);
        
        //set the image as icon of jlabel
        jLabel1.setIcon(imageIcon);
    }

