    MonthlyReport(int year, String month, int projectIndex) throws SQLException {
        //initialise userDAO
        this.user = new UsersDAO();
        //pass all user list from database into Java ArrayList
        usslist = user.getAllUsers();
        //get month from previous class
        this.month = month;
        //initialise IssuesDAO
        iss = new IssuesDAO();
        //get all issue of certain project ID from database and pass into Java ArrayList
        isslist = iss.getAllIssue(projectIndex + 1);
        //loop through the issuelist
        for (int i = 0; i < isslist.size(); i++) {
            //get timestamp of current issue
            long issueTimeStamp = isslist.get(i).getTimestamp();
            //convert timestamp into Date
            Date date1 = new Date(issueTimeStamp * 1000L);
            //convert Date into LocalDate
            LocalDate date = date1.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
            //if current issue created year is same as the year choose from the jtree
            if (date.getYear() == year) {
                //if current issue created month is same as the year choose from the jtree
                if (date.getMonth().toString().equalsIgnoreCase(this.month)) {
                    //add into ArrayList of issue of same month
                    issueofsamemonth.add(isslist.get(i));
                }
            }
        }
        //call method generateReport() to determine value of all details needed to be displayed
        generatereport();
        //initialise components of the GUI
        initComponents();
        this.setLocationRelativeTo(null);
        //if there are no issue in this month, notify user
        if (issueofsamemonth.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Nothing happened this month", "Empty Month", JOptionPane.PLAIN_MESSAGE);
            this.dispose();
        } else {
            this.setVisible(true);
        }

        this.setDefaultCloseOperation(HIDE_ON_CLOSE);

    }

