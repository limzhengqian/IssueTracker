    private void generatereport() {
        //get top scorer
        //**********************************************************************************************************************************************************
        //loop through the user ArrayList
        for (int i = 0; i < usslist.size(); i++) {
            //if user's point is higher than maxscore
            if (usslist.get(i).getPoints() > maxscore) {
                //clear the max score list
                maxscorenamelist.clear();
                //add the maxscore user into the max score list
                maxscorenamelist.add(usslist.get(i).getUsername());
                //update max score to the maxscore user's point
                maxscore = usslist.get(i).getPoints();
            } //if user's point is equal to max score, means multiple winners exist
            else if (usslist.get(i).getPoints() == maxscore) {
                //add the user into the maxscorelist
                maxscorenamelist.add(usslist.get(i).getUsername());
            }
        }
        //set the maxscorename into a string to be displayed
        for (int i = 0; i < maxscorenamelist.size(); i++) {
            maxname += maxscorenamelist.get(i) + ", ";
        }
        //get Tag Count
        //**********************************************************************************************************************************************************
        //initialise a ArrayList to store tag
        ArrayList<String> list = new ArrayList();
        for (int i = 0; i < issueofsamemonth.size(); i++) {
            //loop through each issue's tag Arraylist
            for (int j = 0; j < issueofsamemonth.get(i).getTag().size(); j++) {
                //if the tag of current issue is not in the list
                if (!list.contains(issueofsamemonth.get(i).getTag().get(j))) {
                    //if the tag is null
                    if (issueofsamemonth.get(i).getTag().get(j).equalsIgnoreCase("")) {
                        //add as untag
                        if (!list.contains("Untag")) {
                            list.add("Untag");
                        }
                    } else {
                        //add into the list
                        list.add(issueofsamemonth.get(i).getTag().get(j));
                    }
                }
            }
        }
        //initialise a 2D array to differentiate count and tag name
        arrTag = new String[list.size()][2];
        //loop throught the 2D arary
        for (int i = 0; i < arrTag.length; i++) {
            //add the tag from ArrayList into the 2D array and and initialise tag count as 0
            arrTag[i][0] = list.get(i);
            arrTag[i][1] = "0";
        }

        //loop through list with issue of same day 
        for (int i = 0; i < issueofsamemonth.size(); i++) {
            //loop through tag of current issue
            for (int j = 0; j < issueofsamemonth.get(i).getTag().size(); j++) {
                //loop the 2D array
                for (int k = 0; k < arrTag.length; k++) {
                    if (issueofsamemonth.get(i).getTag().get(j).equalsIgnoreCase("") && arrTag[k][0].equalsIgnoreCase("Untag")) {
                        int count = Integer.parseInt(arrTag[k][1]);
                        count++;
                        arrTag[k][1] = Integer.toString(count);
                    } //if the tag of 2D array is found in the current issue tag
                    else if (issueofsamemonth.get(i).getTag().get(j).equalsIgnoreCase(arrTag[k][0])) {
                        //increase the count of tag insdie 2D array by one
                        int count = Integer.parseInt(arrTag[k][1]);
                        count++;
                        arrTag[k][1] = Integer.toString(count);
                    }
                }
            }
        }
        //determine the tag that appears the most 
        //**********************************************************************************************************************************************************
        //loop through the 2D array
        for (int i = 0; i < arrTag.length; i++) {
            //if count of current tag> max tag, replace it
            if (Integer.parseInt(arrTag[i][1]) > maxTag) {
                maxTagname = arrTag[i][0];
                maxTag = Integer.parseInt(arrTag[i][1]);
            } //if there exist a tag of same ammount, stack it
            else if (Integer.parseInt(arrTag[i][1]) == maxTag) {
                maxTagname = maxTagname + " " + arrTag[i][0];
            }
        }
        //determine the count of status for each week
        //**********************************************************************************************************************************************************
        //loop all the issue in the same month
        for (int i = 0; i < issueofsamemonth.size(); i++) {
            //get timestamp
            long timestamp = issueofsamemonth.get(i).getTimestamp();
            //conver timestamp into Date
            Date date = new Date(timestamp * 1000L);
            //convert Date into LocalDate
            LocalDate ld = date.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
            //if current issue's day is between 1 to 7
            if (ld.getDayOfMonth() >= 1 && ld.getDayOfMonth() <= 7) {
                //issueofweek1++
                issue1++;
                //if status of that issue is closed, issueresolved of week1++
                if (issueofsamemonth.get(i).getStatus().equalsIgnoreCase("Closed")) {
                    issueResolved1++;
                } //if status of that issue is open, issueunresolved of week 1++
                else if (issueofsamemonth.get(i).getStatus().equalsIgnoreCase("Open")) {
                    issueUnresolved1++;
                } //if status of that issue is inprogress, issueinprogress of week1++
                else if (issueofsamemonth.get(i).getStatus().equalsIgnoreCase("In Progress")) {
                    issueinProgress1++;
                }
            } //if current issues's day is between 8 - 14
            else if (ld.getDayOfMonth() >= 8 && ld.getDayOfMonth() <= 14) {
                //issueofweek2++
                issue2++;
                //if status of that issue is closed, issueresolved of week2++
                if (issueofsamemonth.get(i).getStatus().equalsIgnoreCase("Closed")) {
                    issueResolved2++;
                } //if status of that issue is copen, issueunresolved of week2++
                else if (issueofsamemonth.get(i).getStatus().equalsIgnoreCase("Open")) {
                    issueUnresolved2++;
                } //if status of that issue is in progress, issueinprogress of week2++
                else if (issueofsamemonth.get(i).getStatus().equalsIgnoreCase("In Progress")) {
                    issueinProgress2++;
                }
            } //if current issues's day is between 15 - 21
            else if (ld.getDayOfMonth() >= 15 && ld.getDayOfMonth() <= 21) {
                //issueofweek3++
                issue3++;
                //if status of that issue is closed, issueresolved of week3++
                if (issueofsamemonth.get(i).getStatus().equalsIgnoreCase("Closed")) {
                    issueResolved3++;
                } //if status of that issue is open, issueunresolved of week3++
                else if (issueofsamemonth.get(i).getStatus().equalsIgnoreCase("Open")) {
                    issueUnresolved3++;
                } //if status of that issue is in progress, issueinprogress of week3++
                else if (issueofsamemonth.get(i).getStatus().equalsIgnoreCase("In Progress")) {
                    issueinProgress3++;
                }
            } //if current issues's day is between 22 - 28
            else if (ld.getDayOfMonth() >= 22 && ld.getDayOfMonth() <= 28) {
                //issueofweek4++
                issue4++;
                //if status of that issue is closed, issueresolved of week4++
                if (issueofsamemonth.get(i).getStatus().equalsIgnoreCase("Closed")) {
                    issueResolved4++;
                } //if status of that issue is open, issueunresolved of week4++
                else if (issueofsamemonth.get(i).getStatus().equalsIgnoreCase("Open")) {
                    issueUnresolved4++;
                } //if status of that issue is in progress, issueinprogress of week4++
                else if (issueofsamemonth.get(i).getStatus().equalsIgnoreCase("In Progress")) {
                    issueinProgress4++;
                }
            } //if current issues's day is between 29 - 31
            else if (ld.getDayOfMonth() >= 29 && ld.getDayOfMonth() <= 31) {
                //issueofweek5++
                issue5++;
                //if status of that issue is closed, issueresolved of week5++
                if (issueofsamemonth.get(i).getStatus().equalsIgnoreCase("Closed")) {
                    issueResolved5++;
                } //if status of that issue is open, issueunresolved of week5++
                else if (issueofsamemonth.get(i).getStatus().equalsIgnoreCase("Open")) {
                    issueUnresolved5++;
                } //if status of that issue is in progress, issueinprogress of week5++
                else if (issueofsamemonth.get(i).getStatus().equalsIgnoreCase("In Progress")) {
                    issueinProgress5++;
                }
            }
        }
        //get issue of each status(w/o considering week)
        //**********************************************************************************************************************************************************
        //loop the ArrayList of issue within the same month
        for (int i = 0; i < issueofsamemonth.size(); i++) {
            //if status of current issue is closed
            if (issueofsamemonth.get(i).getStatus().equalsIgnoreCase("Closed")) {
                //issueresolved ++
                issueResolved++;
            } //if satus of current issue is open
            else if (issueofsamemonth.get(i).getStatus().equalsIgnoreCase("Open")) {
                //issueunresolved++
                issueUnresolved++;
            } //if status of current issue is in progress
            else if (issueofsamemonth.get(i).getStatus().equalsIgnoreCase("In Progress")) {
                //issureinprogress++
                issueinProgress++;
            }
        }

    }

