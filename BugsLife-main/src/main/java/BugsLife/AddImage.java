        CreateComment.setImageFileName(filename);
        
        //if there is a file uploaded by the user
        if (!filename.equals("")) {
            JOptionPane.showMessageDialog(null, "Image successfully added!");
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(null, "Please insert an image to upload", "Error", JOptionPane.ERROR_MESSAGE);
        }
