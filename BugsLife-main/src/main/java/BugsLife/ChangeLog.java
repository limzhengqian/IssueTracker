    //Scales the image of the logo
    private void scaleImage() {
        ImageIcon imageIcon = new ImageIcon("virus.png");
        Image img = imageIcon.getImage();
        Image newImg = img.getScaledInstance(jLabel2.getWidth() - 5, jLabel2.getHeight(), Image.SCALE_SMOOTH);
        imageIcon = new ImageIcon(newImg);
        jLabel2.setIcon(imageIcon);

    }
    public class MyListCellRenderer extends DefaultListCellRenderer {
        @Override
        public Component getListCellRendererComponent(
                JList list, Object value, int index,
                boolean isSelected, boolean cellHasFocus) {
            super.getListCellRendererComponent(list, value, index, isSelected, cellHasFocus);
            
            // get the data of issue changeLog
            History label = (History) value;
            int issueId = label.getIssueId();
            String date = label.getDate();
            String text = label.getChanges();
            
            // display the issue changeLog into the list
            setText("<html>  Issue ID: " + issueId
                    + "<br> Edited on: " + date
                    + "<br>" + text
                    + "<br> __________________________________________________________________");
            return this;
        }

    }

